import fs from "node:fs";
import path from "node:path";
import solc from "solc";

const root = path.resolve(import.meta.dirname, "..");
const source = fs.readFileSync(path.join(root, "contracts", "DexRoundTripSimulator.sol"), "utf8");
const input = {
  language: "Solidity",
  sources: { "DexRoundTripSimulator.sol": { content: source } },
  settings: {
    optimizer: { enabled: true, runs: 200 },
    outputSelection: { "*": { "*": ["evm.deployedBytecode.object"] } },
  },
};
const output = JSON.parse(solc.compile(JSON.stringify(input)));
const fatal = (output.errors ?? []).filter((e) => e.severity === "error");
if (fatal.length) throw new Error(fatal.map((e) => e.formattedMessage).join("\n"));
const bytecode = output.contracts["DexRoundTripSimulator.sol"].DexRoundTripSimulator.evm.deployedBytecode.object;
const destination = path.join(root, "packages", "analysis", "src", "generated", "dex-simulator-bytecode.ts");
fs.mkdirSync(path.dirname(destination), { recursive: true });
fs.writeFileSync(destination, `// Generated by scripts/compile-simulator.mjs.\nexport const DEX_SIMULATOR_RUNTIME = "0x${bytecode}" as const;\n`);
console.log(`Generated ${destination} (${bytecode.length / 2} bytes)`);
